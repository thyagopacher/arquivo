    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        tu         = new Tutorial();
        tu.setCategoria(ca);
        tu.setResposta(ttexto.getText());
        tu.setTitulo(ttitulo.getText());
        String msg = tRN.inserir(tu);
        if(msg != null && !msg.isEmpty()){
            JOptionPane.showMessageDialog(null, msg, "ERRO:", JOptionPane.ERROR_MESSAGE);
        }else{
            obj.add(tu);
            listar();            
            JOptionPane.showMessageDialog(null, "Inserido com sucesso");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        tu.setCategoria(ca);
        tu.setResposta(ttexto.getText());
        tu.setTitulo(ttitulo.getText());
        String msg = tRN.update(tu);
        if(msg != null && !msg.isEmpty()){
            JOptionPane.showMessageDialog(null, msg, "ERRO:", JOptionPane.ERROR_MESSAGE);
        }else{
            for(int i = 0; i < obj_cat.size(); i++){
                if(ca.getCodigo() == obj_cat.get(i).getCodigo()){
                    obj.get(i).setTitulo(tu.getTitulo());
                    obj.get(i).setResposta(tu.getResposta());
                    obj.get(i).setCategoria(ca);
                    listar();
                    break;
                }
            }            
            JOptionPane.showMessageDialog(null, "Atualizado com sucesso");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

