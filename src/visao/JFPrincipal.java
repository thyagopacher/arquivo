   /**lista tudo o que está na tabela**/  
    private void listar() {
            tabela.setModel(new TableModel(titulo, obj.size(), titulo.length));
            for(int i = 0; i < obj.size(); i++){
                    Categoria ca2 = cRN.procura(obj.get(i).getCategoria().getCodigo());
                    tabela.setValueAt(ca2.getNome(), i, 0);
                    tabela.setValueAt(obj.get(i).getTitulo(), i, 1);
            }
            tabela.setAutoCreateRowSorter(true);    
    }
    
